/* C program to write the details of the student*/
#include<stdio.h>
void main()
{
	// Variable Declaration 
	int age;
	float cgpa;
	char initial;
	
	// Initilization
	age = 19;
	cgpa = 7.6;
	initial = 'N';
	
	// Output
	printf("The age of the student is: %d\n",age);
	printf("CGPA of the student is: %f\n",cgpa);
	printf("Surname of the student starts with: %c",initial);
}
